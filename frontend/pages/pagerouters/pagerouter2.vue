			作为家里的第一个大学生，大一报到那天，文竹第一次来到北京。开学一个月，团委、学生会、社团协会……这些陌生的事物迅速进入他的生活，他不知道该如何选择。那个时候“向上社交”还没有成为热词，文竹也没有靠社交获取信息的意识，他的所有决定，几乎都靠直觉。等到文竹做好职业规划，希望在社工领域做出点成绩的时候，他才发现，自己从一开始就选错了赛道。
			</br></br>
			文竹踌躇许久，将花了半个小时才编辑好的微信发给师姐，询问她有没有建议。师姐社工经历非常丰富，是学院的红人，文竹担心自己目的性太强会惹她厌烦，没想到师姐很快就回了消息，还打来语音电话指导他。文竹了解了许多此前未知的信息，他叹了口气：“要是我大一的时候就知道这些，或许一切都会不一样。”
